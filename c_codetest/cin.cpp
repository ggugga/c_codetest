//cin은 character input의 약자로, C++에서 표준 입력 스트림 객체입니다.
//두 수를 입력받고 합을 출력하는 코드입니다.
//빈 칸에 알맞은 코드를 넣으세요.

#include <iostream>

int main() {
    int t1, t2;

    // 수 2개 입력
    std::cin >> t1 >> t2;

    // 두 수의 합 출력
    std::cout << "합은 " << t1 + t2 << " 입니다.";

    return 0;
}

//입력 예시
//1 2
//출력 예시
//합은 3 입니다.